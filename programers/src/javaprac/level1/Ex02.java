package javaprac.level1;

import java.util.Arrays;

public class Ex02 {
	/*
	 * x만큼 간격이 있는 n개의 숫자
	 * 
	 * ▼문제설명
	 * 함수 solution은 정수 x와 자연수 n을 입력받아
	 * x부터 시작해 x씩 증가하는 숫자를 n개 지나는 리스트를 리턴해야합니다.
	 * 다음 제한 조건을 보고, 조건을 만족하는 함수,
	 * solution을 완성해 주세요.
	 * 
	 * ▼제한조건
	 * x는 -10000000 이상, 10000000 이하인 정수입니다.
	 * n은 1000 이하인 자연수입니다.
	 * 
	 */
	
	public long[] solution(int x, int n) {
		long[] answer = new long[n];
		for(int i = 0; i < n; i++) {
			answer[i] = (long)(i+1) * 2;
		}
		return answer;
	}
	
	// 출력 테스트
	public static void main(String[] args) {
		
		Ex02 ex2 = new Ex02();
		long[] result = ex2.solution(2, 5);
		System.out.println(Arrays.toString(result));

	}

}
